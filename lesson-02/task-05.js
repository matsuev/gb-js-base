/**
 * 5. Реализовать основные 4 арифметические операции в виде функций
 * с двумя параметрами. Обязательно использовать оператор return.
 */

function summ(a, b) {
   return a + b;
}

function diff(a, b) {
   return a - b;
}

function mult(a, b) {
   return a * b;
}

function div(a, b) {
   return a / b;
}

var a = 3;
var b = 7;

console.log(summ(a, b));   // 10
console.log(diff(a, b));   // -4
console.log(mult(a, b));   // 21
console.log(div(a, b));    // 0.42857142857142855